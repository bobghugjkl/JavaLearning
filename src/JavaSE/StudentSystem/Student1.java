package JavaSE.StudentSystem;

public class Student1 {
    private String name ;
    private int age;
    private int studaynumber;

    public Student1() {
    }

    public Student1(String name, int age, int studaynumber) {
        this.name = name;
        this.age = age;
        this.studaynumber = studaynumber;
    }

    public String getName() {
        return name;
    }



    public int getAge() {
        return age;
    }



    public int getStudaynumber() {
        return studaynumber;
    }


}
